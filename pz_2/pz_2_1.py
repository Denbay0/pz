def main():
    # Ввод данных
    x = float(input("Введите массу шоколадных конфет (в кг): "))
    a = float(input("Введите стоимость шоколадных конфет (в рублях): "))
    y = float(input("Введите массу ирисок (в кг): "))
    b = float(input("Введите стоимость ирисок (в рублях): "))

    # Проверка корректности данных
    if x <= 0 or y <= 0:
        print("Ошибка: масса конфет должна быть больше нуля.")
        return
    if a <= 0 or b <= 0:
        print("Ошибка: стоимость конфет должна быть больше нуля.")
        return

    # Вычисления
    c_shok = a / x  # Стоимость 1 кг шоколадных конфет
    c_iris = b / y  # Стоимость 1 кг ирисок
    k = c_shok / c_iris  # Во сколько раз шоколадные конфеты дороже

    # Вывод результатов
    print(f"Стоимость 1 кг шоколадных конфет: {c_shok:.2f} руб")
    print(f"Стоимость 1 кг ирисок: {c_iris:.2f} руб")
    print(f"Шоколадные конфеты дороже ирисок в {k:.2f} раз")


if __name__ == "__main__":
    main()